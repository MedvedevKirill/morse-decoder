const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let res = '';
    let str;
    for(let i = 0; i < expr.length; i+= 10)
    {
        str = expr.substring(i, i+10).replace(/^0+/, '');
        str = str.replace(/(10)/g,'.');
        str = str.replace(/(11)/g, '-');
        str = str.replace(/\*+/, ' ');
        res += MORSE_TABLE[str] !== undefined? MORSE_TABLE[str]: str;
    }
    return res;
}

module.exports = {
    decode
}